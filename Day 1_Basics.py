# 1. BUILT-IN FUNCTION: print()
# Core Use: Displaying messages, outputs, or status on the screen.
# Explanation: Prints the exact text or variable value passed inside the parentheses.

print("Welcome to Python Coding")


# 2. BUILT-IN FUNCTION: type() with Numeric & String Variables
# Core Use: Checking the exact data type of a stored variable.
# Explanation: Identifies whether a variable holds a whole number (int) or text data (str).

total_count = 349
zone_name = "charminar"

print(type(total_count))  
print(type(zone_name))   


# 3. DATA TYPE / COLLECTION: list []
# Core Use: Grouping multiple values or text elements into a single ordered sequence.
# Explanation: Stores multiple items inside square brackets, separated by commas.

zones = ["charminar", "khairatabad", "secunderabad"]
print(zones)
